    /*Llenando la tabla de datos*/
    public void llenarTabla() {
        zon = new dao_Zonas();
        eliminarFilas();
        ocultar_columnas();
        int registros = 0;
        String[] m = new String[4];
        for (zonas z : zon.llenarlistacirculardoble(zon.consultazonas())) {
            m[0] = String.valueOf(z.getId());
            m[1] = z.getCodigo();
            m[2] = z.getNombre();
            modelo.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = modelo.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public zonas buscarzona(String n) {
        for (zonas z : zon.consultazonas()) {
            if (z.getNombre().equals(n)) {
                return z;
            }
        }
        return null;
    }

    /*metodo limpiar los campos*/
    public void limpiar() {
        this.nombre.setText("");
        this.codigo.setText("");
    }


