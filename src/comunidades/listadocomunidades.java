    /*Llenando la tabla de datos*/
    public void llenarTabla() {
        comuni = new dao_comunidades();
        eliminarFilas();
        ocultar_columnas();
        int registros = 0;
        String[] m = new String[4];
        for (Comunidades d : comuni.llenarlista(comuni.consultacomunidades())) {
            m[0] = String.valueOf(d.getIdmunicipio());
            m[1] = d.getCodigo();
            m[2] = d.getNombre();
            m[3] = d.getMunicipios().getNombre();
            modelo.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = modelo.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    /*metodo limpiar los campos*/
    public void limpiar() {
        this.nombre.setText("");
        this.codigo.setText("");
        this.combomunicipios.setSelectedIndex(0);
    }

    /*metodo para buscar el id del combo para insertar y tambien modificar*/
    public Municipio buscarID(String nombre) {

        for (Municipio x : muni.llenarlista(muni.consultamunicipios())) {
            if (x.getNombre().equals(nombre)) {
                return x;
            }
        }
        return null;
    }

    /*Metodo para llenar el combo con los municipios registrados en la base de datos*/
    public void llenarcombomunicipios() {
        for (Municipio x : muni.llenarlista(muni.consultamunicipios())) {
            combomunicipios.addItem(x.getNombre());
        }
    }

    /*metodo para buscar comunidades ya ingresadas*/
    public Comunidades buscarcomunidad(String g) {
        for (Comunidades c : comuni.llenarlista(comuni.consultacomunidades())) {
            if (c.getNombre().equals(g)) {
                return c;
            }
        }
        return null;
    }

