    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = this.nombre.getText();
        String codigo = this.codigo.getText();
        String municipionombre = this.combomunicipios.getSelectedItem().toString();
        municipio = buscarID(municipionombre);
        comunidades = new Comunidades();
        if (!this.nombre.getText().isEmpty() && !this.codigo.getText().isEmpty() && this.combomunicipios.getSelectedIndex() != 0) {
            /*seteando los datos para mandarlos a la base de datos*/
            comunidades.setNombre(nombre);
            comunidades.setCodigo(codigo);
            comunidades.setIdmunicipio(municipio.getIdmunicipio());

            comunidades1 = buscarcomunidad(nombre);
            /*SERAN EDITAR O GUARDAR SEGUN EL TEXTO DEL BOTON QUE MANIPULA LOS DATOS PARA ENVIARLOS A LA DB*/
            if (this.btnGuardar.getText().equals("GUARDAR")) {
                if (comunidades1 == null) {
                    comuni.insertar(comunidades);
                    ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                    er.titulo.setText("REGSITRO GUARDADO");
                    er.msj.setText("REGSITRO INGRESADO");
                    er.msj1.setText("SATISFACTORIAMENTE");
                    er.setVisible(true);
                    llenarTabla();
                    limpiar();
                } else {
                    ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
                    er.titulo.setText("ERROR");
                    er.msj.setText("LA COMUNIDAD YA HA SIDO");
                    er.msj1.setText("REGISTRADA");
                    er.setVisible(true);
                }
            } else if (this.btnGuardar.getText().equals("ACTUALIZAR")) {
                comunidades.setIdcomunidades(Integer.parseInt(this.id.getText()));
                comuni.editar(comunidades);
                ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                er.titulo.setText("REGSITRO MODIFICADO");
                er.msj.setText("REGSITRO ACTUALIZADO");
                er.msj1.setText("SATISFACTORIAMENTE");
                er.setVisible(true);
                titulo.setText("Nuevo registro");
                btnGuardar.setBackground(new Color(26, 187, 156));
                btnGuardar.setText("GUARDAR");
                llenarTabla();
                limpiar();
            }
        } else {
            ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
            er.titulo.setText("CAMPOS VACÍOS");
            er.msj.setText("POR FAVOR LLENE LOS CAMPOS");
            er.msj1.setText("SOLICITADOS");
            er.setVisible(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

