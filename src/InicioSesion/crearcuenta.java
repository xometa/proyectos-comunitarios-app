    private void btniniciosesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btniniciosesionActionPerformed
        // TODO add your handling code here:
        if (!this.iduser.getText().isEmpty() && !this.nombre.getText().isEmpty() && !this.apellido.getText().isEmpty()
                && this.fecha.getDate() != null && !this.dui.getText().isEmpty() && !this.nit.getText().isEmpty()
                && !this.profesion.getText().isEmpty() && !this.usuario.getText().isEmpty() && this.tipo.getSelectedIndex() != 0) {
            this.iduser.setText(String.valueOf(generarnumero()));
            Calendar cal;
            int d, m, a;
            cal = this.fecha.getCalendar();
            d = cal.get(Calendar.DAY_OF_MONTH);
            m = cal.get(Calendar.MONTH);
            a = cal.get(Calendar.YEAR) - 1900;

            String nombre = this.nombre.getText();
            String apellido = this.apellido.getText();

            String dui = this.dui.getText();
            String nit = this.nit.getText();
            String profesion = this.profesion.getText();
            String sexo = "";
            if (this.m.isSelected()) {
                sexo = "Masculino";
            } else if (this.f.isSelected()) {
                sexo = "Femenino";
            }
            String usuario = this.usuario.getText();
            String contrasena = this.contrasena.getText();
            String passencrip = Encriptacion.getStringMessageDigest(contrasena, Encriptacion.MD5);
            String tipo = this.tipo.getSelectedItem().toString();

            String codigo = this.codigo.getText();
            int iduser = Integer.parseInt(this.iduser.getText());

            usuarios.setCodigo(codigo);
            usuarios.setNombre(nombre);
            usuarios.setApellidos(apellido);
            usuarios.setFechanacimiento(new Date(a, m, d));
            usuarios.setDui(dui);
            usuarios.setNit(nit);
            usuarios.setProfesion(profesion);
            usuarios.setSexo(sexo);
            usuarios.setUsuario(usuario);
            usuarios.setContraseña(passencrip);
            usuarios.setTipouser(tipo);

            persona = buscarduinit(dui, nit);
            usuarios1 = buscaruser(usuario);
            usuarios.setIdpersona(iduser);
            if (persona == null) {
                if (usuarios1 == null) {
                    du.insertar(usuarios);
                    ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                    er.titulo.setText("REGSITRO GUARDADO");
                    er.msj.setText("REGSITRO INGRESADO");
                    er.msj1.setText("SATISFACTORIAMENTE");
                    er.setVisible(true);
                    limpiar();
                } else {
                    ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
                    er.titulo.setText("ERROR");
                    er.msj.setText("EL USUARIOS YA HA SIDO INGRESADO");
                    er.msj1.setText("POR FAVOR INGRESE OTRO");
                    er.setVisible(true);
                }
            } else {
                ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
                er.titulo.setText("ERROR");
                er.msj.setText("EL DUI Ó NIT YA HAN SIDO INGRESADO");
                er.msj1.setText("POR FAVOR INGRESE OTRO");
                er.setVisible(true);
            }

        } else {
            ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
            er.titulo.setText("CAMPOS VACÍOS");
            er.msj.setText("POR FAVOR LLENE LOS CAMPOS");
            er.msj1.setText("SOLICITADOS");
            er.setVisible(true);
        }
    }//GEN-LAST:event_btniniciosesionActionPerformed

