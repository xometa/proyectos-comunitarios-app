    public int generarnumero() {
        int n = 0;
        if (dp.datospersona().size() > 0) {
            n = dp.datospersona().get(dp.datospersona().size() - 1).getIdpersona() + 1;
        } else {
            n = 1;
        }

        return n;
    }

    /*metodo de la busqueda de usuarios existentes para que no hallan repeticiones*/
    public Usuario buscaruser(String usuario) {
        for (Usuario u : du.llenaravl(du.datosusuarios())) {
            if (u.getUsuario().equals(usuario)) {
                return u;
            }
        }
        return null;
    }

    /*Metodo para la busqueda de el dui y el nit para que no hallan registros repetidos*/
    public Persona buscarduinit(String dui, String nit) {
        for (Persona p : dp.datospersona()) {
            if (p.getDui().equals(dui) || p.getNit().equals(nit)) {
                return p;
            }
        }
        return null;
    }

    /*Metodo para limpiar los campos*/
    public void limpiar() {

        this.codigo.setText("");
        this.nombre.setText("");
        this.apellido.setText("");
        this.fecha.setDate(null);
        this.dui.setText("");
        this.nit.setText("");
        this.profesion.setText("");
        this.sexgroup.clearSelection();
        this.usuario.setText("");
        this.contrasena.setText("");
        this.tipo.setSelectedIndex(0);
    }

