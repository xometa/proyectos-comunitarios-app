    public void llenarTabla(String dato) {
        da = new dao_alcaldes();
        eliminarFilas();
//        ocultar_columnas();
        int registros = 0;
        String[] m = new String[9];
        for (Alcalde d : da.llenarbinario(da.consultaalcaldes(dato))) {
            m[0] = String.valueOf(d.getIdpersona());
            m[1] = d.getNombre();
            m[2] = d.getApellidos();
            m[3] = String.valueOf(d.getFechanacimiento());
            m[4] = d.getDui();
            m[5] = d.getNit();
            m[6] = d.getProfesion();
            m[7] = d.getSexo();
            m[8] = d.getTipo();
            modelo.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = modelo.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    /*metodo de busqueda de la prymari key en el arbol avl para que no hallan repeticiones (aunque el arbol ya valida esta es una validacion adicional)*/
    public Alcalde buscarPrimaryKey(int id) {
        for (Alcalde d : da.llenarbinario(da.consultaalcaldes(""))) {
            if (d.getIdpersona() == id) {
                return d;
            }
        }
        return null;
    }

    /*metodo para la busqueda de alcaldes, con el fin de que no se repitan el dui y el nit*/
    public Alcalde buscaralcalde(String dui, String nit) {
        for (Alcalde d : da.llenarbinario(da.consultaalcaldes(""))) {
            if (d.getDui().equals(dui) || d.getNit().equals(nit)) {
                return d;
            }
        }
        return null;
    }

    /*Metodo para buscar id en toda la tabla persona para que no se repitan ya que otra tabla hereda los datos de la tabla persona*/
    public Persona buscaridpersona(int id) {
        for (Persona p : dp.datospersona()) {
            if (p.getIdpersona() == id) {
                return p;
            }
        }
        return null;
    }

