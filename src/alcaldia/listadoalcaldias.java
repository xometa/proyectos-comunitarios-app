    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        String nombre = this.nombre.getText();
        String nit = this.nit.getText();
        String telefono = this.telefono.getText();
        if (!this.nombre.getText().isEmpty() && !this.nit.getText().isEmpty() && !this.telefono.getText().isEmpty()
                && !this.alcalde.getText().isEmpty() && !this.municipio.getText().isEmpty()) {
            int idalcalde = Integer.parseInt(this.idalcalde.getText());
            int idmunicipio = Integer.parseInt(this.idmunicipio.getText());

            alcaldia.setNombrealcaldia(nombre);
            alcaldia.setNit(nit);
            alcaldia.setTelefono(telefono);
            alcaldia.setIdmunicipio(idmunicipio);
            alcaldia.setIdpersona(idalcalde);
            alcaldia1 = buscarnit(nit);
            if (this.btnGuardar.getText().equals("GUARDAR")) {
                if (alcaldia1 == null) {
                    da.insertar(alcaldia);
                    ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                    er.titulo.setText("REGSITRO GUARDADO");
                    er.msj.setText("REGSITRO INGRESADO");
                    er.msj1.setText("SATISFACTORIAMENTE");
                    er.setVisible(true);
                    llenarTabla();
                    limpiar();
                } else {
                    ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
                    er.titulo.setText("ERROR");
                    er.msj.setText("EL NIT DE LA ALCALDÍA YA HA SIDO REGISTRADO.");
                    er.msj1.setText("POR FAVOR INGRESE OTRO.");
                    er.setVisible(true);
                    JOptionPane.showMessageDialog(null, "\n ");
                }

            } else if (this.btnGuardar.getText().equals("ACTUALIZAR")) {
                alcaldia.setIdalcaldia(Integer.parseInt(this.id.getText()));
                da.editar(alcaldia);
                ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                er.titulo.setText("REGSITRO MODIFICADO");
                er.msj.setText("REGSITRO ACTUALIZADO");
                er.msj1.setText("SATISFACTORIAMENTE");
                er.setVisible(true);
                titulo.setText("Nuevo registro");
                btnGuardar.setBackground(new Color(26, 187, 156));
                btnGuardar.setText("GUARDAR");
                llenarTabla();
                limpiar();
            }
        } else {
            ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
            er.titulo.setText("CAMPOS VACÍOS");
            er.msj.setText("POR FAVOR LLENE LOS CAMPOS");
            er.msj1.setText("SOLICITADOS");
            er.setVisible(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

