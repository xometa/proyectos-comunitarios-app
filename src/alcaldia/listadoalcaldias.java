    /*Llenando la tabla de datos*/
    public void llenarTabla() {
        da = new dao_alcaldia();
        eliminarFilas();
        ocultar_columnas();
        int registros = 0;
        String[] m = new String[8];
        for (Alcaldia d : da.llenarlista(da.mostraralcaldias())) {
            m[0] = String.valueOf(d.getIdalcaldia());
            m[1] = d.getNombrealcaldia();
            m[2] = d.getNit();
            m[3] = d.getTelefono();
            m[4] = d.getMunicipio().getNombre();
            m[5] = d.getAlcalde().getNombre() + " " + d.getAlcalde().getApellidos();
            m[6] = String.valueOf(d.getMunicipio().getIdmunicipio());
            m[7] = String.valueOf(d.getIdpersona());
            modelo.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = modelo.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    /*Metodo para buscar el nit de la alcaldia para que no se repita*/
    public Alcaldia buscarnit(String nit) {

        for (Alcaldia a : da.llenarlista(da.mostraralcaldias())) {
            if (a.getNit().equals(nit)) {
                return a;
            }
        }
        return null;
    }

