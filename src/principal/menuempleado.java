    public String mostrarFecha() {
        Date fecha = new Date();
        //FORMATO PARA LA FECHA
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/YYYY");
        return format.format(fecha);

    }

    public String mostrarDia() {

        String diaActual = null;
        int dia;
        dia = calendar.get(Calendar.DAY_OF_WEEK);
        switch (dia) {
            case 1:
                diaActual = "Domingo";
                break;
            case 2:
                diaActual = "Lunes";
                break;
            case 3:
                diaActual = "Martes";
                break;
            case 4:
                diaActual = "Miercoles";
                break;
            case 5:
                diaActual = "Jueves";
                break;
            case 6:
                diaActual = "Viernes";
                break;
            case 7:
                diaActual = "Sabado";
                break;
        }
        return diaActual;
    }

    public void mostrarHora() {
        Calendar calendario = new GregorianCalendar();
        Date horaActual = new Date();
        calendario.setTime(horaActual);
        hora = calendario.get(Calendar.HOUR_OF_DAY) > 9 ? "" + calendario.get(Calendar.HOUR_OF_DAY) : "0" + calendario.get(Calendar.HOUR_OF_DAY);
        minutos = calendario.get(Calendar.MINUTE) > 9 ? "" + calendario.get(Calendar.MINUTE) : "0" + calendario.get(Calendar.MINUTE);
        segundos = calendario.get(Calendar.SECOND) > 9 ? "" + calendario.get(Calendar.SECOND) : "0" + calendario.get(Calendar.SECOND);
    }

    public void run() {
        Thread correr = Thread.currentThread();
        while (correr == hilo) {
            mostrarHora();
            this.lblhora.setText(hora + ":" + minutos + ":" + segundos);
            this.lblhora2.setText(hora + ":" + minutos + ":" + segundos);
        }
    }
    
