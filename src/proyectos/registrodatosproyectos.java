    /*Llenando la tabla de datos*/
    public void llenarTabla() {
        dp = new dao_proyectos();
        eliminarFilas();
        ocultar_columnas();
        String[] m = new String[13];
        for (Proyectos d : dp.llenarlista(dp.mostrartodosproyectos())) {
            m[0] = String.valueOf(d.getIdproyecto());
            m[1] = d.getCodigoproyecto();
            m[2] = d.getDescripción();
            m[3] = String.valueOf(d.getMonto());
            m[4] = String.valueOf(d.getFechainicia());
            m[5] = String.valueOf(d.getFechafinaliza());
            m[6] = d.getEstado();
            m[7] = d.getAlcaldia().getNombrealcaldia();
            m[8] = String.valueOf(d.getIdalcaldia());
            m[9] = d.getComunidades().getNombre();
            m[10] = String.valueOf(d.getIdcomunidad());
            m[11] = d.getUsuario().getNombre() + " " + d.getUsuario().getApellidos();
            m[12] = String.valueOf(d.getIduser());
            model.addRow(m);
        }
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = model.getRowCount();
            for (int i = 0; filas > i; i++) {
                model.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    /*Metodo para buscar un proyecto existente*/
    public Proyectos buscarproy(String descripcion, String comunidad) {
        for (Proyectos p : dp.mostrartodosproyectos()) {
            if (p.getDescripción().equals(descripcion) && p.getComunidades().getNombre().equals(comunidad)) {
                return p;
            }
        }
        return null;
    }

