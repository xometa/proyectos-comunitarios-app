    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        don = new Donantes();
        donantes = new dao_donantes();
        String nombre = this.nombre.getText();
        String empresa = this.empresa.getText();
        String email = this.email.getText();
        String direccion = this.direccion.getText();
        String postal = this.postal.getText();
        String ciudad = this.ciudad.getText();
        String telefono = this.telefono.getText();
        String tipo = this.tipo.getSelectedItem().toString();
        String pais = this.pais.getText();

        if (!this.nombre.getText().isEmpty() && !this.empresa.getText().isEmpty() && !this.email.getText().isEmpty()
                && !this.direccion.getText().isEmpty() && !this.postal.getText().isEmpty() && !this.ciudad.getText().isEmpty()
                && !this.telefono.getText().isEmpty() && this.tipo.getSelectedIndex() != 0) {
            if (this.tipo.getSelectedItem().equals("Extranjero")) {
                don.setNombre(nombre);
                don.setEmpresa(empresa);
                don.setEmail(email);
                don.setDireccion(direccion);
                don.setCodigopostal(postal);
                don.setCiudad(ciudad);
                don.setTelefono(telefono);
                don.setTipo(tipo);
                don.setPais(pais);
            } else if (this.tipo.getSelectedItem().equals("Nacional")) {
                don.setNombre(nombre);
                don.setEmpresa(empresa);
                don.setEmail(email);
                don.setDireccion(direccion);
                don.setCodigopostal(postal);
                don.setCiudad(ciudad);
                don.setTelefono(telefono);
                don.setTipo(tipo);
                don.setPais("");
            }
            if (this.btnGuardar.getText().equals("GUARDAR")) {
                donantes.insertar(don);
                JOptionPane.showMessageDialog(null, "Guardado con exito");
                limpiar();
            } else if (this.btnGuardar.getText().equals("ACTUALIZAR")) {
                don.setIddonantes(Integer.parseInt(this.id.getText()));
                donantes.editar(don);
                JOptionPane.showMessageDialog(null, "Cambios con exito");
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campos vacios");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tipoItemStateChanged
        // TODO add your handling code here:
        if (this.tipo.getSelectedItem().equals("Seleccione:")) {
            jLabel7.setVisible(false);
            this.pais.setVisible(false);
        } else if (this.tipo.getSelectedItem().equals("Extranjero")) {
            jLabel7.setVisible(true);
            this.pais.setVisible(true);
        } else if (this.tipo.getSelectedItem().equals("Nacional")) {
            jLabel7.setVisible(false);
            this.pais.setVisible(false);
        }
    }//GEN-LAST:event_tipoItemStateChanged

    /*metodo limpiar los campos*/
    public void limpiar() {
        this.nombre.setText("");
        this.empresa.setText("");
        this.email.setText("");
        this.direccion.setText("");
        this.postal.setText("");
        this.ciudad.setText("");
        this.telefono.setText("");
        this.pais.setText("");
        this.tipo.setSelectedIndex(0);
    }

