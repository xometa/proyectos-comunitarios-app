    /*Llenando la tabla de datos*/
    public void llenarTabla() {
        donantes = new dao_donantes();
        eliminarFilas();
        ocultar_columnas();
        int registros = 0;
        String[] m = new String[10];
        for (Donantes d : donantes.llenarlista(donantes.consultadonantes())) {
            m[0] = String.valueOf(d.getIddonantes());
            m[1] = d.getNombre();
            m[2] = d.getEmpresa();
            m[3] = d.getEmail();
            m[4] = d.getDireccion();
            m[5] = d.getCodigopostal();
            m[6] = d.getCiudad();
            m[7] = d.getTelefono();
            m[8] = d.getTipo();
            m[9] = d.getPais();
            model.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = model.getRowCount();
            for (int i = 0; filas > i; i++) {
                model.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public Donantes buscarempresa(String g) {
        for (Donantes d : donantes.llenarlista(donantes.consultadonantes())) {
            if (d.getEmpresa().equals(g)) {
                return d;
            }
        }
        return null;
    }

