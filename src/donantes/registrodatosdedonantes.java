    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        dn = new Donantes();
        String nombre = this.nombre.getText();
        String empresa = this.empresa.getText();
        String email = this.email.getText();
        String direccion = this.direccion.getText();
        String postal = this.postal.getText();
        String ciudad = this.ciudad.getText();
        String telefono = this.telefono.getText();
        String tipo = this.tipo.getSelectedItem().toString();
        String pais = this.pais.getText();

        if (!this.nombre.getText().isEmpty() && !this.empresa.getText().isEmpty() && !this.email.getText().isEmpty()
                && !this.direccion.getText().isEmpty() && !this.postal.getText().isEmpty() && !this.ciudad.getText().isEmpty()
                && !this.telefono.getText().isEmpty() && this.tipo.getSelectedIndex() != 0) {
            if (this.tipo.getSelectedItem().equals("Extranjero")) {
                dn.setNombre(nombre);
                dn.setEmpresa(empresa);
                dn.setEmail(email);
                dn.setDireccion(direccion);
                dn.setCodigopostal(postal);
                dn.setCiudad(ciudad);
                dn.setTelefono(telefono);
                dn.setTipo(tipo);
                dn.setPais(pais);
            } else if (this.tipo.getSelectedItem().equals("Nacional")) {
                dn.setNombre(nombre);
                dn.setEmpresa(empresa);
                dn.setEmail(email);
                dn.setDireccion(direccion);
                dn.setCodigopostal(postal);
                dn.setCiudad(ciudad);
                dn.setTelefono(telefono);
                dn.setTipo(tipo);
                dn.setPais("");
            }
            dn1 = buscarempresa(empresa);
            if (this.btnGuardar.getText().equals("GUARDAR")) {
                if (dn1 == null) {
                    donantes.insertar(dn);
                    ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                    er.titulo.setText("REGSITRO GUARDADO");
                    er.msj.setText("REGSITRO INGRESADO");
                    er.msj1.setText("SATISFACTORIAMENTE");
                    er.setVisible(true);
                    llenarTabla();
                    limpiar();
                } else {
                    ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
                    er.titulo.setText("ERROR");
                    er.msj.setText("LA EMPRESA YA HA SIDO INGRESADA");
                    er.msj1.setText("");
                    er.setVisible(true);
                }
            } else if (this.btnGuardar.getText().equals("ACTUALIZAR")) {
                dn.setIddonantes(Integer.parseInt(this.id.getText()));
                donantes.editar(dn);
                ProcesoExitoso er = new ProcesoExitoso(new JFrame(), true);
                er.titulo.setText("REGSITRO MODIFICADO");
                er.msj.setText("REGSITRO ACTUALIZADO");
                er.msj1.setText("SATISFACTORIAMENTE");
                er.setVisible(true);
                titulo1.setText("Nuevo registro");
                btnGuardar.setBackground(new Color(26, 187, 156));
                btnGuardar.setText("GUARDAR");
                llenarTabla();
                limpiar();
            }
        } else {
            ErrordeProcesos er = new ErrordeProcesos(new JFrame(), true);
            er.titulo.setText("CAMPOS VACÍOS");
            er.msj.setText("POR FAVOR LLENE LOS CAMPOS");
            er.msj1.setText("SOLICITADOS");
            er.setVisible(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

