    public void llenarTabla() {
        du = new dao_usuarios();
        eliminarFilas();
        ocultar_columnas();
        int registros = 0;
        String[] m = new String[13];
        for (Usuario d : du.llenaravl(du.datosusuarios())) {
            m[0] = String.valueOf(d.getIdpersona());
            m[1] = d.getCodigo();
            m[2] = d.getNombre();
            m[3] = d.getApellidos();
            m[4] = d.nombrecompleto();
            m[5] = String.valueOf(d.getFechanacimiento());
            m[6] = d.getDui();
            m[7] = d.getNit();
            m[8] = d.getProfesion();
            m[9] = d.getSexo();
            m[10] = d.getUsuario();
            m[11] = d.getContraseña();
            m[12] = d.getTipouser();
            modelo.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = modelo.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public int generarnumero() {
        int n = 0;
        if (dp.datospersona().size() > 0) {
            n = dp.datospersona().get(dp.datospersona().size() - 1).getIdpersona() + 1;
        } else {
            n = 1;
        }

        return n;
    }

    /*metodo de la busqueda de usuarios existentes para que no hallan repeticiones*/
    public Usuario buscaruser(String usuario) {
        for (Usuario u : du.llenaravl(du.datosusuarios())) {
            if (u.getUsuario().equals(usuario)) {
                return u;
            }
        }
        return null;
    }

    /*Metodo para la busqueda de el dui y el nit para que no hallan registros repetidos*/
    public Persona buscarduinit(String dui, String nit) {
        for (Persona p : dp.datospersona()) {
            if (p.getDui().equals(dui) || p.getNit().equals(nit)) {
                return p;
            }
        }
        return null;
    }

