    public void llenarTabla(String dato, String cap) {
        dpr = new dao_preliquidaciones();
        eliminarFilas();
        ocultar_columnas();
        llenarcolita(dato);
        mostrarimportes(cap);
        int registros = 0;
        String[] m = new String[10];
        for (Preliquidaciones p : dpr.llenarlistacircular(dpr.mostrarpreliquidaciones(dato, cap))) {
            m[0] = String.valueOf(p.getIdpreliquidacion());
            m[1] = p.getClasedeobra();
            m[2] = p.getCapitulo();
            m[3] = String.valueOf(p.getPrecioproyecto());
            m[4] = String.valueOf(p.getUnidadesproyecto());
            m[5] = String.valueOf(p.getImporteproyecto());
            m[6] = String.valueOf(p.getUnidadespreliquidaciones());
            m[7] = String.valueOf(p.getImportepreliquidaciones());
            m[8] = String.valueOf(p.getDiferencia());
            m[9] = String.valueOf(p.getIdproyecto());
            model.addRow(m);
            registros += 1;
        }
        this.totregistros.setText(String.valueOf(registros));
    }

    /*Metodo de eliminacion de las filas para que no se dupliquen los datos al mostrarlos al registrar uno nuevo*/
    public void eliminarFilas() {
        try {
            int filas = model.getRowCount();
            for (int i = 0; filas > i; i++) {
                model.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede limpiar la tabla", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    /*busqueda del id del proyecto*/
    public Proyectos buscaridproyecto(String dato) {
        for (Proyectos p : dp.mostrarproyectos("")) {
            if (p.getDescripción().equals(dato)) {
                return p;
            }
        }
        return null;
    }

    /*Metodo para la lista*/
    public void llenarlista() {
        dp = new dao_proyectos();
        for (Proyectos p : dp.mostrarproyectosnocancelados()) {
            modelolista.addElement(p.getDescripción());
        }
    }

    /*metodo para buscar la clase de obra para que no se repita el dato*/
    public Preliquidaciones buscarclaseobra(String obra) {
        for (Preliquidaciones pr : dpr.llenarlistacircular(dpr.mostrarpreliquidaciones(this.id.getText(), jLabel18.getText()))) {
            if (pr.getClasedeobra().equals(obra)) {
                return pr;
            }
        }
        return null;
    }

    /*llenando la cola con todos los capitulos*/
    public void llenarcolita(String id) {
        llenarCola(dpr.mostrarcola(id));
    }

    /*llenando la cola con cada datos de los productos*/
    public ArrayList<Preliquidaciones> llenarCola(ArrayList<Preliquidaciones> a) {
        for (int i = 0; i < a.size(); i++) {
            colacapitulos.offer(a.get(i));
        }
        return colacapitulos.mostrar();
    }

    /*Metodo para sacar de la cola y actualizar el saldo de cada capitulo y los diferentes se volveran a meter*/
    public void mostrarimportes(String capitulo) {
        Preliquidaciones pr = new Preliquidaciones();
        /*variables para las importaciones*/
        double importepro = 0;
        double importepre = 0;
        double diferencia = 0;
        while (!colacapitulos.isEmpty()) {
            pr = ((Preliquidaciones) colacapitulos.poll());
            if (pr.getCapitulo().equals(capitulo)) {
                importepro += pr.getImporteproyecto();
                importepre += pr.getImportepreliquidaciones();
                diferencia += pr.getDiferencia();
            }
        }
        this.importproy.setText(String.valueOf(importepro));
        this.importpre.setText(String.valueOf(importepre));
        this.diferenciapre.setText(String.valueOf(diferencia));
    }

