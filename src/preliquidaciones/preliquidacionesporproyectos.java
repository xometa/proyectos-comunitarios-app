                double importepreliquidacion = Double.parseDouble(this.importeliquidaciones.getText());

                preliquidacion.setClasedeobra(claseobra);
                preliquidacion.setCapitulo(capitulo);
                preliquidacion.setPrecioproyecto(precioproyecto);
                preliquidacion.setUnidadesproyecto(unidadesproyecto);
                preliquidacion.setImporteproyecto(importeproyectos);
